    private void pripremiFormu() {
        srediTabeluRacun();
    }

    private void srediTabeluRacun() {

        jTableStavkaRacuna.setModel(new ModelTabelRacun(racun));
        JComboBox jComboBoxKnjige = new JComboBox();

        ZahtevObjekat zahtev = new ZahtevObjekat();
        zahtev.setOperacija(IOperacije.VRATI_KNJIGE);
        zahtev.setParametar("");
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(zahtev);

        OdgovorObjekat odgovor = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Knjiga> listaKnjiga = (ArrayList<Knjiga>) odgovor.getOdgovor();

        for (Knjiga knjiga : listaKnjiga) {
            jComboBoxKnjige.addItem(knjiga);
        }
        TableColumn tcKnjige = jTableStavkaRacuna.getColumnModel().getColumn(0);
        tcKnjige.setCellEditor(new DefaultCellEditor(jComboBoxKnjige));
    }
