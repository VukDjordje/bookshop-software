    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed

        racun.setSifraRacuna(Integer.parseInt(jTextFieldRacunID.getText().trim()));
        racun.setRadnik(ulogovaniRadnik);
        racun.pripremiRacun();
        jTextFieldUkupanIznos.setText("" + racun.getUkupanIznos());

        ZahtevObjekat zahtev = new ZahtevObjekat();
        zahtev.setOperacija(IOperacije.UBACI_RACUN);
        zahtev.setParametar(racun);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(zahtev);

        OdgovorObjekat odgovor = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        if (odgovor.getStatus() == IStatus.OK) {
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio racun!", "Uspesno cuvanje racuna!", JOptionPane.INFORMATION_MESSAGE);
            jTextFieldRacunID.setText("");
            ModelTabelRacun model = (ModelTabelRacun) jTableStavkaRacuna.getModel();
            model.clear();
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti racun!!", "Uspesno cuvanje racuna!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

