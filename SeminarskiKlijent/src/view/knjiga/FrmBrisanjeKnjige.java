    private void jButtonPronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPronadjiActionPerformed
        String filter = jTextFieldFilter.getText();
        String nalepi = "";
        
        if(jRadioButtonNaziv.isSelected()){
            nalepi = "WHERE k.naziv LIKE '"+filter+"' ORDER BY k.naziv ASC";
        }
        
        if(jRadioButtonGodina.isSelected()){
            nalepi = "WHERE k.godina LIKE '"+filter+"' ORDER BY k.godina ASC";
        }
        
        if(jRadioButton1.isSelected()){
            nalepi = "WHERE a.prezime LIKE '"+filter+"' ORDER BY a.prezime ASC";
        }
        
        srediTabelu(nalepi);
        JOptionPane.showMessageDialog(this, "Sistem je nasao knjigu po trazenoj vrednosti!", "Pretrazi", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButtonPronadjiActionPerformed

    private void jButtonDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetaljiActionPerformed
        int red = jTableKnjige.getSelectedRow();
        if(red != -1){
            ModelTabelKnjiga model = (ModelTabelKnjiga) jTableKnjige.getModel();
            Knjiga knjiga = model.vratiKnjigu(red);
            JOptionPane.showMessageDialog(this, "Naziv: "+knjiga.getNaziv()+"\n Cena: "+knjiga.getCena(), "Detalji", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati knjigu!", "Detalji", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDetaljiActionPerformed

