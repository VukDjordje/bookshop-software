    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
       int red = jTableKnjige.getSelectedRow();
        if(red != -1){
            ModelTabelKnjiga model = (ModelTabelKnjiga) jTableKnjige.getModel();
            Knjiga knjiga = model.vratiKnjigu(red);
            
            Napisana napisana = new Napisana(knjiga, null);
            
            ZahtevObjekat zahtev = new ZahtevObjekat();
            zahtev.setOperacija(IOperacije.IZBRISI_KNJIGU);
            zahtev.setParametar(napisana);
            KomunikacijaSaServerom.getInstanca().posaljiZahtev(zahtev);
            
            OdgovorObjekat odgovor = KomunikacijaSaServerom.getInstanca().primiOdgovor();
            if(odgovor.getStatus()==IStatus.OK){
                JOptionPane.showMessageDialog(this, "Uspesno obrisana knjiga!!", "Obrisi", JOptionPane.INFORMATION_MESSAGE);
                srediTabelu("");
            } else {
                JOptionPane.showMessageDialog(this, "Neuspesno obrisana knjiga!!", "Obrisi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati knjigu!", "Detalji", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

